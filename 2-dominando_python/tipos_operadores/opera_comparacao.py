# operadores de comparação : a saída é falsa ou verdadeira

# igualdade
saldo =450
saque =204
print (saldo == saque)

# diferença
saldo =450
saque =204
print (saldo != saque)

# maior que/ maior ou igual
saldo =450
saque =204

print (saldo > saque)
print (saldo >= saque)

# menor que/ menor ou igual
saldo =450
saque =204

print (saldo < saque)
print (saldo <= saque)

