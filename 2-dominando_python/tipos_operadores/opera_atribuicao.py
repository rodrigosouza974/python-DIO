# atribuição simples
saldo = 500

print(saldo)

# atribuição com adição
saldo = 500
saldo += 200 # forma reduzida de :saldo = saldo + 200
print(saldo)

# atribuição com subtração
saldo = 500
saldo -= 200 # forma reduzida de :saldo = saldo - 200
print(saldo)

# atribuição com multiplicação
saldo = 500
saldo *= 2 # forma reduzida de :saldo = saldo * 2
print(saldo)

# atribuição com divisão
saldo = 500
saldo /= 20 # forma reduzida de :saldo = saldo / 20
print(saldo) # resultado com ponto flutuante

saldo = 500
saldo //= 20 
print(saldo) # resultado inteiro

# atribuição com módulo
saldo = 500
saldo %= 2 # forma reduzida de :saldo = saldo % 2
print(saldo)

# atribuição com exponeciação
saldo = 500
saldo **= 2 # forma reduzida de :saldo = saldo ** 2
print(saldo)